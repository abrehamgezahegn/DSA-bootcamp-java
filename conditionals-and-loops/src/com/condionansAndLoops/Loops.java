package com.condionansAndLoops;

public class Loops {
    public static void main(String[] args) {
        for (int i = 0; i < 5 ; i++) {
            System.out.println("Hello world");
        }

        int num = 0;
        while(num < 5 ){
            System.out.println("Hello world");
            num++;
        }

        int num2  = 0;
        do{
            System.out.println("Hello");
        }while (num2 !=1 );
    }
}
